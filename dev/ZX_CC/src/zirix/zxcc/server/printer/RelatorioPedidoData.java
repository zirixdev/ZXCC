package zirix.zxcc.server.printer;


public class RelatorioPedidoData {  

	private String nomeVendedor; 
	private String nomeCliente; 
	private String fantasiaCliente; 


	public String getNomeVendedor() { 
		return nomeVendedor; 
	}  
	public void setNomeVendedor(String nome) { 
		this.nomeVendedor = nome; 
	}  

	public String getNomeCliente() { 
		return nomeCliente; 
	}  
	public void setNomeCliente(String nome) { 
		this.nomeCliente = nome; 
	}  
	public String getFantasiaCliente() { 
		return fantasiaCliente; 
	}  
	public void setFantasiaCliente(String nome) { 
		this.fantasiaCliente = nome; 
	} 
}
